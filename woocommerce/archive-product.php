    $(document).ready(function() {
        function initFlexSlider() {
            $(".flexslider").each(function() {
                if (window.innerWidth <= 768) {
                    max = 2;
                    min = 2;
                } else if (window.innerWidth <= 1024) {
                    max = 3;
                    min = 3;
                } else {
                    max = $(this).data("max");
                    min = $(this).data("min");
                }
                width = eval($(this).data("width"));
                if (typeof max === "undefined") {
                    max = "6";
                }
                $(this).flexslider({
                    animation: "slide",
                    animationLoop: false,
                    itemWidth: width,
                    itemMargin: 20,
                    minItems: min,
                    maxItems: max,
                    controlNav: false,
                    directionNav: true,
                    touch: true
                });
            })
        }

        initFlexSlider();

        $(window).resize(function() {
            $(".flexslider").flexslider("destroy");
            initFlexSlider();
        });
    });
